package com.xdtech.report.controller;

public class GloomyFishSummaryInfoBean {
	private String name;
	private String nickName;
	private String regDate;
	private String blogURL;
	private String majorDomain;
	private int workYears;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getBlogURL() {
		return blogURL;
	}
	public void setBlogURL(String blogURL) {
		this.blogURL = blogURL;
	}
	public String getMajorDomain() {
		return majorDomain;
	}
	public void setMajorDomain(String majorDomain) {
		this.majorDomain = majorDomain;
	}
	public int getWorkYears() {
		return workYears;
	}
	public void setWorkYears(int workYears) {
		this.workYears = workYears;
	}

}
